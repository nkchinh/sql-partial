using System.Collections.Immutable;
using System.Text;
using SqlPartial.Generator.Models;

namespace SqlPartial.Generator.Core
{
    internal static class SourceBuilder
    {
        // ── SqlStrings struct ────────────────────────────────────────────────

        /// <summary>
        /// Generates the SqlStrings struct definition.
        /// Called once per project when ExternalSqlStringsType is not set.
        /// </summary>
        public static string BuildSqlStringsStruct(GeneratorConfig config, bool supportsNullable)
        {
            var sb = new StringBuilder();
            var ns = config.SqlStringsNamespace;

            sb.AppendLine("// <auto-generated/>");
            if (supportsNullable)
            {
                sb.AppendLine(config.NullableEnabled ? "#nullable enable" : "#nullable disable");
            }

            sb.AppendLine($"namespace {ns}");
            sb.AppendLine("{");
            sb.AppendLine("    /// <summary>");
            sb.AppendLine("    /// Holds SQL query strings for each configured DBMS provider.");
            sb.AppendLine("    /// Use <see cref=\"Get\"/> to retrieve the appropriate string at runtime.");
            sb.AppendLine("    /// </summary>");
            sb.AppendLine("    public readonly struct SqlStrings");
            sb.AppendLine("    {");

            // AnsiSql property — always present
            sb.AppendLine("        /// <summary>ANSI SQL — shared fallback for all providers.</summary>");
            sb.AppendLine("        public string AnsiSql { get; }");
            sb.AppendLine();

            // One property per configured provider
            foreach (var providerName in config.DistinctProviderNames)
            {
                sb.AppendLine($"        /// <summary>{providerName} specific SQL. Falls back to <see cref=\"AnsiSql\"/> when null.</summary>");
                var type = supportsNullable ? "string?" : "string";
                sb.AppendLine($"        public {type} {providerName} {{ get; }}");
                sb.AppendLine();
            }

            // Constructor
            var paramType = supportsNullable ? "string?" : "string";
            sb.Append("        public SqlStrings(string ansiSql");
            foreach (var providerName in config.DistinctProviderNames)
            {
                sb.Append($", {paramType} {providerName.ToLowerInvariant()} = null");
            }
            sb.AppendLine(")");
            sb.AppendLine("        {");
            sb.AppendLine("            AnsiSql = ansiSql;");
            foreach (var providerName in config.DistinctProviderNames)
            {
                sb.AppendLine($"            {providerName} = {providerName.ToLowerInvariant()};");
            }
            sb.AppendLine("        }");
            sb.AppendLine();

            // Implicit conversion to string
            sb.AppendLine("        /// <summary>Implicit conversion to string returns <see cref=\"AnsiSql\"/>.</summary>");
            sb.AppendLine("        public static implicit operator string(SqlStrings s) => s.AnsiSql;");
            sb.AppendLine();

            // Get(string providerName) method
            sb.AppendLine("        /// <summary>");
            sb.AppendLine("        /// Returns the SQL string for the given provider name (from appsettings),");
            sb.AppendLine("        /// falling back to <see cref=\"AnsiSql\"/> when no specific string is configured.");
            sb.AppendLine("        /// </summary>");
            sb.AppendLine("        public string Get(string providerName)");
            sb.AppendLine("        {");
            sb.AppendLine("            switch (providerName)");
            sb.AppendLine("            {");
            foreach (var providerName in config.DistinctProviderNames)
            {
                sb.AppendLine($"                case \"{providerName}\":");
                sb.AppendLine($"                    return {providerName} ?? AnsiSql;");
            }
            sb.AppendLine("                default:");
            sb.AppendLine("                    return AnsiSql;");
            sb.AppendLine("            }");
            sb.AppendLine("        }");
            sb.AppendLine("    }");
            sb.AppendLine("}");

            return sb.ToString();
        }

        // ── Partial class ────────────────────────────────────────────────────

        /// <summary>
        /// Generates one partial class file containing all SqlStrings properties
        /// for a single ClassName within a namespace.
        /// </summary>
        public static string BuildPartialClass(
            string ns,
            string className,
            ImmutableArray<SqlQueryGroup> groups,
            GeneratorConfig config,
            bool supportsNullable)
        {
            // Resolve the SqlStrings type name to use
            var stringsType = config.ExternalSqlStringsType ?? $"{config.SqlStringsNamespace}.SqlStrings";

            var sb = new StringBuilder();

            sb.AppendLine("// <auto-generated/>");
            if (supportsNullable)
            {
                sb.AppendLine(config.NullableEnabled ? "#nullable enable" : "#nullable disable");
            }

            sb.AppendLine($"namespace {ns}");
            sb.AppendLine("{");
            sb.AppendLine($"    partial class {className}");
            sb.AppendLine("    {");

            foreach (var group in groups)
            {
                var ansiContent = group.GetContent(FilePathParser.AnsiSqlProviderName);
                sb.AppendLine($"        private static readonly {stringsType} Sql{group.QueryName} = new {stringsType}(");
                sb.Append($"            @\"{ansiContent}\"");

                // Provider-specific — only emit when the file exists for that provider name
                foreach (var providerName in config.DistinctProviderNames)
                {
                    if (group.ContentByProviderName.TryGetValue(providerName, out var content))
                    {
                        sb.AppendLine(",");
                        sb.Append($"            {providerName.ToLowerInvariant()}: @\"{content}\"");
                    }
                }

                sb.AppendLine(");");
                sb.AppendLine();
            }

            sb.AppendLine("    }");
            sb.AppendLine("}");

            return sb.ToString();
        }
    }
}
